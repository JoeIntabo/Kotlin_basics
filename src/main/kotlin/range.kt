fun main(args: Array<String>) {
    print("Number Range:")

    for (num in 3..9) {
        println(num)
    }
    print("Number Range:")

    for (number in 10..15){
        println(number)
    }

    print("Character ranges:")
    for (char in 'c'..'i'){
        println(char)
    }
    print("Character ranges:")
    for (character in 'a'..'z'){
        println(character)
    }




}